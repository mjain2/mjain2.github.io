        <a name="backgroundnovo"></a>
        <h5>Background:</h5>
            <p>Novoalign is a software that aligns sequencing reads to a reference database(i.e.HG19) and calls variants to identify regions in the sequences that may be of significance. The software supports fragment, paired-end reads and is designed as an accurate and quick short read mapper.  Novoalign can be used for small to large genomes.</p>
            <p> Specifics: Novoalign 2.08.03 for Illumina paired-end reads.</p>
        <!How do we use it?>
        <a name="usenovo"></a>
        <h5>How do we use it?</h5>
            <p>Novoalign's maker - Novocraft - provides some documentation on how to use Novoalign. The website provided a formula to follow under their Quick Start Tutorial:</p> 
            
        <p style="font-family:'Inconsolata', sans-serif;font-size: 13px">           novoalign -d reference_genome -f filename1 filename2 -i mean, stdev -o SAM </p>
        
            <p> To run it, a reference genome had to be created.  The genome was created using these commands:</p>
            <p style="font-family:'Inconsolata', sans-serif;font-size: 13px">
            module load novocraft
            <br />novoindex ref.nix ref.fa
            </p>
            <p>An example of the command needed to actually run the aligner is:</p>
            <p style="font-family:'Inconsolata', sans-serif;font-size: 13px"> 
            novoalign -d ref.nix -f ../Sample/4_2_10_TAGCTT_L001_R1_001.fastq.gz ../Sample/4_2_10_TAGCTT_L001_R2_001.fastq.gz -i 250,90 -o SAM > L001_001.SAM </p>
            <p>(To calculate the mean and standard deviation of the size distribution of the sequencing runs, a short python program was created. However, you could also use this command: </p>
            <p style="font-family:'Inconsolata', sans-serif; font-size:13px"> novoalign -d genome.nix -f read1.fq read2.fq -i 500,100 -#2K >/some/output</p> 
            <p> This command will give the first 2000 alignments of two files, and the mean and standard deviation of the fragment lengths of the sequence will be displayed at the end of the file.  As long as the -i parameters are robust enough for the read [ex. -i 500,100], the alignment will be fairly accurate.)</p>
            
