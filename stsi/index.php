            <a name="qstatconky"></a>
            <h5> qstat Command </h5>
            <p> Once I was satisfied with how conky looked, I worked on creating a new section in it that reflected the 'qstat' function of what was occuring in a remote host.  The 'qstat' function allows one to see what jobs are currently running.  To create this, I inserted the following code into my .conkyrc file [note: the names (ex. remote@host.edu) should be changed to reflect your specific remote hosts and usernames, etc].</p>
            <p style="font-family:'Inconsolata', sans-serif;font-size: 13px">
            <br /> Remote Host Name ${hr 2}
            <br /> ${execi 600 ssh user@remotehost.edu 'uptime'}
            <br /> ${execi 600 ssh user@remotehost.edu 'qstat | grep username | gawk "{ print \$1 \": \" \$3 \"\t\" \"State: \" \$5 }"'}
            </p>
            <p> The number after 'execi' tells you how after how many seconds the command repeats.  In this case, this command would run every 10 minutes ('600'). By using 'grep username', you will only see jobs that are under your name (this is especially useful if there are many people running jobs on the remote host). The information inside the print statement tells you what part of the 'qstat' will be displayed on conky. 
            <p> [The alternative 'print' command below will show the status of the job (ex. 'R' for running), the running time of the job, and then the job name:] 
            <p style="font-family: 'Inconsolata', sans-serif;font-size: 13px">
            ${execi 10 ssh user@remotehost.edu 'qstat | grep username | gawk "{ print \$5 \": \" \$4 \"\t\" \$2 }"'}</p>
            
