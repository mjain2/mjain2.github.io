        <!Overview>
        <a name="overviewchisq"></a>
        <h5>Overview:</h5>
        <p>A Chi-Squared test is statistical test that compares observed counts of categorical data with expected counts of the same categorical data.  To calculate the expected counts of a 2x2 contigency table you follow this formula:<br />
        <br />
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Expected = Row Total X Column Total / Total Counts <br />
        <br />
        To get the Chi-Squared value you follow this formula:<br />
        <br />
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Chi-Squared = Î£ [(observed count - expected count)^2/expected count]<br />
        <br />
        Using the Chi-Squared value you can then measure statistical significance by calculating the p-value. The Chi-Squared test utilizes a null hypothesis, or the hypothesis that the two categorical variables are independent of each other. If the p-value is less than the assigned level of significance (in most cases, this value is 0.05), then you REJECT the null hypothesis and conclude that the two categorical variables are NOT independent.</p>
        <p>   
        This <a href="http://math.hws.edu/javamath/ryan/ChiSquare.html" style="color: #80BCA3" target="_blank"> link</a> provides a great and detailed explanation of the Chi-Squared test. </p>
        
        <!GWAS>
        <a name="gwaschisq"></a>
        <h5>What is a GWAS?</h5>
        <p>A Genome Wide Association Study (or GWAS for short) compares a 'control' genome (ex. HG19) with a 'case' genome (ex. your genome) and genotypes both samples for common single-nucleotide polymorhism's (or SNPs for short).  A GWAS aims to determine if allelic frequency of SNPs between the two genomes is different. </p>
        <p> To determine this, we do an odds ratio.  When an odds ratio is 1 it means that both SNPs have equal odds of happening.  A Chi-Squared is then used to test the significance of the odds ratio. </p>
        
